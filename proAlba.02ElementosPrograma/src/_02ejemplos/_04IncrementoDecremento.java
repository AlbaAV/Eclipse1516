package _02ejemplos;

public class _04IncrementoDecremento {

	public static void main(String[] args) {
		
		int num = 5;
		System.out.println(num);
		num = num +1;
		System.out.println(num);
		num++; //Es lo mismo que num = num +1
		System.out.println(num);
		
		
		num = num -1;
		System.out.println(num);
		num--; // Es igual que num =num - 1;
		System.out.println(num);
		
		
		
	}
}
